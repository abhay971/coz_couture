import { useRef, useEffect } from 'react'
import gsap from 'gsap'
import { ScrollTrigger } from 'gsap/ScrollTrigger'
import { navLinks, socialLinks, companyInfo } from '../../data/navigation'
import { useCursor } from '../../context/CursorContext'

gsap.registerPlugin(ScrollTrigger)

export function Footer() {
  const { setHovering, resetCursor } = useCursor()
  const footerRef = useRef(null)
  const contentRef = useRef(null)
  const currentYear = new Date().getFullYear()

  useEffect(() => {
    const ctx = gsap.context(() => {
      // Content reveal animation
      const elements = contentRef.current?.querySelectorAll('.footer-animate')
      if (elements) {
        gsap.fromTo(
          elements,
          { opacity: 0, y: 30 },
          {
            opacity: 1,
            y: 0,
            duration: 0.8,
            stagger: 0.1,
            ease: 'power3.out',
            scrollTrigger: {
              trigger: footerRef.current,
              start: 'top 85%',
            },
          }
        )
      }
    }, footerRef)

    return () => ctx.revert()
  }, [])

  const scrollToSection = (href) => {
    const element = document.querySelector(href)
    if (element) {
      element.scrollIntoView({ behavior: 'smooth' })
    }
  }

  const scrollToTop = () => {
    window.scrollTo({ top: 0, behavior: 'smooth' })
  }

  return (
    <footer ref={footerRef} className="bg-coz-charcoal text-white">
      {/* Main Footer Content */}
      <div ref={contentRef} className="py-16 lg:py-20">
        <div className="container-padding">
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-12 lg:gap-8">
            {/* Brand Section */}
            <div className="lg:col-span-1 footer-animate">
              <div className="mb-6">
                <img
                  src="/logo/COZ_COUTURE_White.png"
                  alt="COZ COUTURE"
                  className="h-12 w-auto"
                />
                <span className="text-gray-300 tracking-wide font-bold mb-6 text-sm sm:text-base">
                  CLUB of Lifestyle Products
                </span>
              </div>
              
            </div>

            {/* Navigation */}
            <div className="footer-animate">
              <h4 className="text-xs font-bold tracking-[0.2em] text-white/40 uppercase mb-6">
                Quick Links
              </h4>
              <ul className="space-y-3">
                {navLinks.map((link) => (
                  <li key={link.id}>
                    <button
                      onClick={() => scrollToSection(link.href)}
                      className="text-white/70 hover:text-coz-orange transition-colors text-sm"
                      onMouseEnter={() => setHovering(true)}
                      onMouseLeave={() => resetCursor()}
                    >
                      {link.label}
                    </button>
                  </li>
                ))}
              </ul>
            </div>

            {/* Contact Info */}
            <div className="footer-animate">
              <h4 className="text-xs font-bold tracking-[0.2em] text-white/40 uppercase mb-6">
                Contact
              </h4>
              <div className="space-y-3">
                <a
                  href={`mailto:${companyInfo.email}`}
                  className="block text-white/70 hover:text-coz-orange transition-colors text-sm"
                  onMouseEnter={() => setHovering(true)}
                  onMouseLeave={() => resetCursor()}
                >
                  {companyInfo.email}
                </a>
                <p className="text-white/70 text-sm">{companyInfo.phone}</p>
                <p className="text-white/50 text-sm">
                  Mumbai, Maharashtra<br />
                  India
                </p>
              </div>
            </div>

            {/* Social & Back to Top */}
            <div className="footer-animate">
              <h4 className="text-xs font-bold tracking-[0.2em] text-white/40 uppercase mb-6">
                Follow Us
              </h4>
              <div className="flex gap-3 mb-8">
                {socialLinks.map((social) => (
                  <a
                    key={social.id}
                    href={social.href}
                    target="_blank"
                    rel="noopener noreferrer"
                    className="w-10 h-10 border border-white/20 rounded-full flex items-center justify-center text-white/70 hover:border-coz-orange hover:text-coz-orange transition-all duration-300"
                    onMouseEnter={() => setHovering(true)}
                    onMouseLeave={() => resetCursor()}
                    aria-label={social.label}
                  >
                    <SocialIcon name={social.icon} />
                  </a>
                ))}
              </div>

              {/* Back to Top */}
              <button
                onClick={scrollToTop}
                onMouseEnter={() => setHovering(true)}
                onMouseLeave={() => resetCursor()}
                className="group flex items-center gap-3 text-white/50 hover:text-white transition-colors"
              >
                <span className="w-10 h-10 rounded-full border border-white/20 flex items-center justify-center group-hover:border-coz-orange transition-colors">
                  <svg
                    className="w-4 h-4 group-hover:-translate-y-0.5 transition-transform"
                    fill="none"
                    stroke="currentColor"
                    viewBox="0 0 24 24"
                  >
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 10l7-7m0 0l7 7m-7-7v18" />
                  </svg>
                </span>
                <span className="text-xs tracking-wider uppercase">Back to Top</span>
              </button>
            </div>
          </div>
        </div>
      </div>

      {/* Bottom Bar */}
      <div className="py-6 border-t border-white/10">
        <div className="container-padding">
          <div className="flex flex-col md:flex-row items-center justify-between gap-4 text-sm text-white/40">
            <p>&copy; {currentYear} COZ COUTURE. All rights reserved.</p>
            <p>Made in India, for the World</p>
          </div>
        </div>
      </div>
    </footer>
  )
}

